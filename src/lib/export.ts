// ── Markdown ──────────────────────────────────────────────

export function summaryToMarkdown(summary: MeetingSummary): string {
  const lines: string[] = ['# Meeting Summary', ''];

  lines.push('## Executive Summary', '', summary.executiveSummary, '');

  if (summary.keyDecisions.length > 0) {
    lines.push('## Key Decisions', '');
    summary.keyDecisions.forEach((d) => lines.push(`- ${d}`));
    lines.push('');
  }

  if (summary.actionItems.length > 0) {
    lines.push('## Action Items', '');
    summary.actionItems.forEach((item) => {
      let line = `- [ ] ${item.task}`;
      if (item.assignee) line += ` (${item.assignee})`;
      if (item.deadline) line += ` -- by ${item.deadline}`;
      lines.push(line);
    });
    lines.push('');
  }

  if (summary.topicsDiscussed.length > 0) {
    lines.push('## Topics Discussed', '');
    summary.topicsDiscussed.forEach((t) => lines.push(`- ${t}`));
    lines.push('');
  }

  if (summary.participants.length > 0) {
    lines.push('## Participants', '');
    summary.participants.forEach((p) => lines.push(`- ${p}`));
    lines.push('');
  }

  if (summary.followUpQuestions.length > 0) {
    lines.push('## Follow-up / Open Items', '');
    summary.followUpQuestions.forEach((q) => lines.push(`- ${q}`));
    lines.push('');
  }

  return lines.join('\n');
}

// ── Plain Text ────────────────────────────────────────────

export function summaryToPlainText(summary: MeetingSummary): string {
  const lines: string[] = ['MEETING SUMMARY', ''];

  lines.push('EXECUTIVE SUMMARY', summary.executiveSummary, '');

  if (summary.keyDecisions.length > 0) {
    lines.push('KEY DECISIONS');
    summary.keyDecisions.forEach((d) => lines.push(`  * ${d}`));
    lines.push('');
  }

  if (summary.actionItems.length > 0) {
    lines.push('ACTION ITEMS');
    summary.actionItems.forEach((item) => {
      let line = `  [ ] ${item.task}`;
      if (item.assignee) line += ` (${item.assignee})`;
      if (item.deadline) line += ` -- by ${item.deadline}`;
      lines.push(line);
    });
    lines.push('');
  }

  if (summary.topicsDiscussed.length > 0) {
    lines.push('TOPICS DISCUSSED');
    summary.topicsDiscussed.forEach((t) => lines.push(`  * ${t}`));
    lines.push('');
  }

  if (summary.participants.length > 0) {
    lines.push('PARTICIPANTS');
    summary.participants.forEach((p) => lines.push(`  * ${p}`));
    lines.push('');
  }

  if (summary.followUpQuestions.length > 0) {
    lines.push('FOLLOW-UP / OPEN ITEMS');
    summary.followUpQuestions.forEach((q) => lines.push(`  * ${q}`));
    lines.push('');
  }

  return lines.join('\n');
}


// ── HTML (email-friendly) ─────────────────────────────────

function buildHtmlSection(title: string, items: string[], ordered = false): string {
  if (items.length === 0) return '';
  const tag = ordered ? 'ol' : 'ul';
  const listItems = items.map((i) => `<li style="margin-bottom:4px">${i}</li>`).join('');
  return `<h2 style="color:#333;font-size:16px;margin:20px 0 8px">${title}</h2><${tag} style="padding-left:20px;color:#444">${listItems}</${tag}>`;
}

export function summaryToHtml(summary: MeetingSummary): string {
  let html = `<!DOCTYPE html><html><head><meta charset="utf-8"></head>
<body style="font-family:Arial,Helvetica,sans-serif;max-width:640px;margin:0 auto;padding:24px;color:#222;line-height:1.6">
<h1 style="font-size:20px;color:#111;border-bottom:2px solid #8b5cf6;padding-bottom:8px">Meeting Summary</h1>
<h2 style="color:#333;font-size:16px;margin:20px 0 8px">Executive Summary</h2>
<p style="color:#444">${summary.executiveSummary}</p>`;

  html += buildHtmlSection('Key Decisions', summary.keyDecisions);

  if (summary.actionItems.length > 0) {
    const items = summary.actionItems.map((a) => {
      let text = a.task;
      if (a.assignee) text += ` <strong>(${a.assignee})</strong>`;
      if (a.deadline) text += ` <em>by ${a.deadline}</em>`;
      return text;
    });
    html += buildHtmlSection('Action Items', items);
  }

  html += buildHtmlSection('Topics Discussed', summary.topicsDiscussed);
  html += buildHtmlSection('Participants', summary.participants);
  html += buildHtmlSection('Follow-up / Open Items', summary.followUpQuestions);

  html += `<hr style="border:none;border-top:1px solid #ddd;margin:24px 0">
<p style="font-size:12px;color:#999">Generated by Meeting Summarizer &mdash; agenticsis.top</p>
</body></html>`;

  return html;
}

